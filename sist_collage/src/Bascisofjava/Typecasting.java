package Bascisofjava;

public class Typecasting {
	
	public static void main(String [] args) {
		    //Implicit
		byte b=127;
		int i=b;
		System.out.println(i);
		
		float f1=23.05f;
		double d=f1;
		System.out.println(d);
		
		
		//Explict casting
		
		double d1=34.05d;
		long l1=(long)d1;
		System.out.println(l1);
		
	}
}
